    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        String email = textField1.getText();
        String mobile = textField2.getText();
        String fname = textField3.getText();
        String lname = textField4.getText();
        String nic = textField5.getText();
        boolean validate = false;

        if (mobile.isEmpty()) {
            notifi.show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Please enter your mobile number");
        } else if (email.isEmpty()) {
            notifi.show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Please enter your email");
        } else if (fname.isEmpty()) {
            notifi.show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Please enter your first name");
        } else if (lname.isEmpty()) {
            notifi.show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Please enter your last name");
        } else if (nic.isEmpty()) {
            notifi.show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Please enter your NIC number");
        } else if (!mobile.matches("\\d{10}")) {
            notifi.show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Invalid Mobile Number");
        } else if (!email.matches("^[a-zA-Z0-9.!#$%&'*+/=?^_`{|}~-]+@([a-zA-Z0-9-]+\\.)+[a-zA-Z0-9]{2,5}$")) {
            notifi.show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Invalid Email");
        } else if (!nic.matches("^(([5,6,7,8,9]{1})([0-9]{1})([0,1,2,3,5,6,7,8]{1})([0-9]{6})([v|V|x|X]))|(([1,2]{1})([0,9]{1})([0-9]{2})([0,1,2,3,5,6,7,8]{1})([0-9]{7}))$")) {
            notifi.show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Invalid NIC");
        } else {
            validate = true;
        }

        if (!isUpdate) {

            if (validate) {
                try {
                    ResultSet  rs = mySql.executeSearch("SELECT * `customer` WHERE `email` = '"+email+"' OR `nic` = '"+nic+"' OR `mobile` = '"+mobile+"' ");
                    if (!rs.next()) {
                        mySql.executeIUD("INSERT INTO `customer` (`email`,`mobile`,`first_name`,`last_name`,`nic`)"
                            + " VALUES ('" + email + "','" + mobile + "','" + fname + "','" + lname + "','" + nic + "')");
                        reset();
                        loadCustomer("email","ASC","empty");
                        notifi.show(Notifications.Type.SUCCESS, Notifications.Location.TOP_CENTER, "Customer added successfully");
                    }else{
                        reset();
                        notifi.show(Notifications.Type.ERROR, Notifications.Location.TOP_CENTER, "Customer Already In");
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }

        } else if (isUpdate) {
            if (validate) {
                try {
                    mySql.executeIUD("UPDATE `customer` "
                        + "SET `mobile` = '"+mobile+"' , `first_name` = '"+fname+"' ,`last_name` = '"+lname+"' ,"
                        + "`nic`='"+nic+"' WHERE `email` ='"+email+"' ");
                    notifi.show(Notifications.Type.SUCCESS, Notifications.Location.TOP_CENTER, "update  successfully");
                } catch (Exception e) {
                    notifi.show(Notifications.Type.ERROR, Notifications.Location.TOP_CENTER, "update Faild Duplicate Data");
                    e.printStackTrace();
                }
                reset();
                loadCustomer("email","ASC","empty");

            }
        }
    }//GEN-LAST:event_button1ActionPerformed

