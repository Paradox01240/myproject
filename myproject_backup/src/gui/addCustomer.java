    private void loadCustomer(String column,String order ,String search) {
        try {
           ResultSet rs ;
            if (search.equals("empty")) {
                 rs = mySql.executeSearch("SELECT * FROM `customer` ORDER BY `"+column+"` "+order+"");
            }else{
                 rs = mySql.executeSearch("SELECT * FROM `customer` WHERE `first_name` LIKE '"+search+"%' OR `email` LIKE '"+search+"' ORDER BY `"+column+"` "+order+"");
            }
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            while (rs.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(rs.getString("id"));
                vector.add(rs.getString("mobile"));
                vector.add(rs.getString("email"));
                vector.add(rs.getString("first_name") + " " + rs.getString("last_name"));
                vector.add(rs.getString("nic"));
                vector.add("More");
                model.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

